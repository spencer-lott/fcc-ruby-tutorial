# Variables can be declared and reused over and over again
character_name = "Raisin Face"
character_age = "35"

puts "There once was a man named " + character_name +";"
puts "he was " + character_age + " years old."

# Variables can also be reassigned later on (for our example from Rasin Face to Tom)
character_name = "Tom"

puts "He really liked the name " + character_name
puts "but didn't like being " + character_age + "."
